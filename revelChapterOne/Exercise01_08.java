package revelChapterOne;

public class Exercise01_08 {

	public static void main(String[] args) {
		/* Write a program that displays the area and perimeter
		 * of a circle that has a radius of 5.5 using the following
		 * formula:
		 * perimeter = 2 x radius x pi
		 * area = radius x radius x pi
		 */

		System.out.print("The area of the circle is ");
		System.out.println(5.5 * 5.5 * Math.PI);
		
		System.out.print("The perimeter of the circle is ");
		System.out.println(2.0 * 5.5 * Math.PI);
	}

}
