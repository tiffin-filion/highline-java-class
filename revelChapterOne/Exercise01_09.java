package revelChapterOne;

public class Exercise01_09 {

	public static void main(String[] args) {
		/* Write a program that displays the area and perimeter of
		 * a rectangle with the width of 4.5 and height of 7.9
		 * using the following formula:
		 * area = width x height
		 */

		System.out.print("The area of the rectangle is ");
		System.out.println(4.5 * 7.9);
		
		System.out.print("The perimeter of the rectangle is ");
		System.out.println(2 * (4.5 + 7.9));
		
	}

}
